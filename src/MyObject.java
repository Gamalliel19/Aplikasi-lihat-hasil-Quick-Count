public class MyObject {
    private String nama;
    private double quick;

    public MyObject(String nama, double quick) {
        this.nama = nama;
        this.quick = quick;
    }
    
    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public double getQuick() {
        return quick;
    }

    public void setQuick(double quick) {
        this.quick = quick;
    }
    
    
}
